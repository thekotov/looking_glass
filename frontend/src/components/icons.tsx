// Tiny SVG icon set used by the NavBar (and reusable elsewhere).
//
// Paths come from the Lucide icon library (MIT). Inlined to avoid pulling
// in a whole icon package — each icon is ~150 bytes. All icons share the
// same 24-viewbox / 1.75-stroke / round-cap style so they line up visually.

import type { SVGProps } from "react";

type IconProps = SVGProps<SVGSVGElement> & { size?: number };

function Icon({ size = 14, children, ...rest }: IconProps & { children: React.ReactNode }) {
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth={1.75}
      strokeLinecap="round"
      strokeLinejoin="round"
      aria-hidden="true"
      {...rest}
    >
      {children}
    </svg>
  );
}

